export const JD_DS_STRINGS: Record<JdDsLocale, Record<JdDsStringKey, string>> = {
  hi: hi as Record<JdDsStringKey, string>,
  en,
};

export function toJdDsLocale(language: string | null | undefined): JdDsLocale {
  return language === "en" ? "en" : "hi";
}

export function jdDsT(
  locale: JdDsLocale,
  key: JdDsStringKey,
  vars?: Record<string, string | number>
): string {
  const raw = JD_DS_STRINGS[locale][key] ?? JD_DS_STRINGS.hi[key] ?? key;
  if (!vars) return raw;
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replaceAll(`{${k}}`, String(v)),
    raw
  );
}

export const JD_DS_KEY_COUNT = Object.keys(hi).length;
