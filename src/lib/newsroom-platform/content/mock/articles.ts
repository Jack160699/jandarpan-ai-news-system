function hoursAgo(h: number): string {
  return new Date(Date.now() - h * 60 * 60 * 1000).toISOString();
}

function article(
  partial: Omit<PlatformArticle, "seo" | "aiSummary"> & {
    seo?: Partial<PlatformArticle["seo"]>;
  }
): PlatformArticle {
  const path = `/story/${partial.slug}`;
  return {
    ...partial,
    seo: {
      title: partial.seo?.title ?? `${partial.title} | Jan Darpan`,
      description: partial.seo?.description ?? partial.excerpt,
      keywords: partial.seo?.keywords ?? partial.tags,
      canonicalPath: path,
      ogImage: partial.image,
    },
    aiSummary: partial.excerpt.slice(0, 120),
  };
}

